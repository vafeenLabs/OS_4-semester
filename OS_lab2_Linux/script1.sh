#!/bin/bash

# Проверяем, что переданы три аргумента
if [ "$#" -ne 3 ]; then
    echo "Использование: $0 <процесс1> <процесс2> <процесс3>"
    exit 1
fi

# Отображаем информацию о заданных процессах в реальном режиме
top -p $1,$2,$3

# Ожидаем нажатия кнопки R
echo "Для переназначения приоритета одному из процессов нажмите R"
while true; do
    read -n1 key
    if [ "$key" == "R" ] || [ "$key" == "r" ]; then
        break
    fi
done

# Переназначаем приоритет одному из процессов
echo "Введите PID процесса, которому нужно изменить приоритет:"
read pid
echo "Введите новый приоритет для процесса (от -20 до 19):"
read priority

renice $priority $pid

echo "Приоритет процесса с PID $pid изменен на $priority"

